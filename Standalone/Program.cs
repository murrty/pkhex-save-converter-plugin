    class Program {
        static void Main(string[] args) {
            var desmumeFooter = new byte[] { 0x7C, 0x3C, 0x2D, 0x2D, 0x53, 0x6E, 0x69, 0x70, 0x20, 0x61, 0x62, 0x6F, 0x76, 0x65, 0x20, 0x68, 0x65, 0x72, 0x65, 0x20, 0x74, 0x6F, 0x20, 0x63, 0x72, 0x65, 0x61, 0x74, 0x65, 0x20, 0x61, 0x20, 0x72, 0x61, 0x77, 0x20, 0x73, 0x61, 0x76, 0x20, 0x62, 0x79, 0x20, 0x65, 0x78, 0x63, 0x6C, 0x75, 0x64, 0x69, 0x6E, 0x67, 0x20, 0x74, 0x68, 0x69, 0x73, 0x20, 0x44, 0x65, 0x53, 0x6D, 0x75, 0x4D, 0x45, 0x20, 0x73, 0x61, 0x76, 0x65, 0x64, 0x61, 0x74, 0x61, 0x20, 0x66, 0x6F, 0x6F, 0x74, 0x65, 0x72, 0x3A, 0x01, 0xF8, 0x07, 0x00, 0x00, 0x00, 0x08, 0x00, 0x06, 0x00, 0x00, 0x00, 0x03, 0x00, 0x00, 0x00, 0x00, 0x00, 0x08, 0x00, 0x00, 0x00, 0x00, 0x00, 0x7C, 0x2D, 0x44, 0x45, 0x53, 0x4D, 0x55, 0x4D, 0x45, 0x20, 0x53, 0x41, 0x56, 0x45, 0x2D, 0x7C };
            if (args.Length > 0) {
                if (File.Exists(args[0])) {
                    try {
                        if (args[0].EndsWith("dsv")) {
                            using (var fs = new FileStream(args[0], FileMode.Open, FileAccess.Write)) {
                                fs.SetLength(Math.Max(0, fs.Length - 122));
                            }
                            File.Move(args[0], args[0].Substring(0, args[0].Length - 3) + "sav");
                        }
                        else if (args[0].EndsWith("sav")) {
                            using (var fs = new FileStream(args[0], FileMode.Append, FileAccess.Write)) {
                                fs.Write(desmumeFooter, 0, desmumeFooter.Length);
                            }
                            File.Move(args[0], args[0].Substring(0, args[0].Length - 3) + "dsv");
                        }
                    }
                    catch (Exception ex) {
                        Console.WriteLine(ex.ToString());
                        Console.ReadKey();
                    }
                }
            }
            else {
                Console.WriteLine("Drag + Drop a .SAV or .DSV file here to convert between DesMuME Save and RAW Save");
            }
            Environment.Exit(0);
        }
    }
}
